					<p>Многие россияне, в том числе молодожёны или работники организаций, находящихся на бюджетном финансировании, задумываются о покупке собственного жилья. Но из-за кризиса и прочих насущных проблем большинству это не по карману. Выход из, казалось бы, тупиковой ситуации – социальная ипотека.</p>
					<p>Социальная ипотека – всероссийская программа. Она помогает получить собственное жилье социально незащищенным гражданам, а также очередникам, с нехваткой средств для покупки недвижимости по программе коммерческого кредитования.</p>
					<p>Условия предоставления льгот разнятся в каждом регионе, в некоторых частях страны есть собственные программы социальной помощи. Для уточнения информации необходимо обратиться в жилищные органы города. Там дадут консультацию по всем видам программ, первом взносе, сроке кредита, а также процентной ставке.</p>
